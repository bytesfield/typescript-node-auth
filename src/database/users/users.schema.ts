import { Schema } from "mongoose";

const UserSchema = new Schema({
    firstname: {
        type: String,
        required: true,
        min: 6,
        max: 255
    },
    lastname: {
        type: String,
        required: true,
        min: 6,
        max: 255
    },
    email: {
        type: String,
        required: true,
        unique: true,
        lowercase: true,
        min: 6,
        max: 255
    },
    password: {
        type: String,
        required: true,
        min: 6,
        max: 1024
    },
    role: {
        type: String,
        default: "user",
    },
    status: {
        type: String,
        default: "pending",
    }

}, { timestamps: true });

export default UserSchema;